import { createClient } from "@supabase/supabase-js";

const supabaseUrl = import.meta.env.VITE_SUPABASE_URL;
const supabaseKey = import.meta.env.VITE_SUPABASE_ANON_KEY || import.meta.env.VITE_SUPABASE_PUBLISHABLE_KEY;

if (!supabaseUrl || !supabaseKey) {
  console.error(
    "⚠️ SUPABASE: Configure as variáveis VITE_SUPABASE_URL e VITE_SUPABASE_ANON_KEY no Easypanel ou .env"
  );
}

export const supabase = createClient(supabaseUrl ?? "", supabaseKey ?? "");

/**
 * Creates a Supabase client with the wallet address header
 * for RLS-protected mutations.
 */
export function getWalletClient(wallet: string) {
  return createClient(supabaseUrl ?? "", supabaseKey ?? "", {
    global: {
      headers: { "x-wallet-address": wallet },
    },
  });
}
